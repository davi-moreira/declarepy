def _lm_lin_est(
    y: str = "Y",
    z: str = "Z",
    covariates: Sequence[str] = ("X",),
    weights: Optional[str] = None,
    inquiry: Optional[str] = None,
    label: str = "estimator",
) -> Estimator:
    """estimatr's ``lm_lin``: Y ~ Z * (covariates − their (weighted) mean)."""

    covs = list(covariates)

    def fn(df: pd.DataFrame) -> EstimatorResult:
        n = len(df)
        zv = df[z].to_numpy(dtype=float)
        yv = df[y].to_numpy(dtype=float)
        w = df[weights].to_numpy(dtype=float) if weights is not None else None
        centered = []
        for c in covs:
            xv = df[c].to_numpy(dtype=float)
            center = float(np.average(xv, weights=w)) if w is not None else float(xv.mean())
            centered.append(xv - center)
        X = np.column_stack(
            [np.ones(n), zv, *centered, *[zv * xc for xc in centered]]
        )
        beta, se, dof = _hc2_core(X, yv, w)
        return _tidy(float(beta[1]), float(se[1]), dof, term=z, outcome=y)

    return Estimator(fn, inquiry=inquiry, label=label)


def _iv_robust_est(
    y: str = "Y",
    d: str = "D",
    z: str = "Z",
    inquiry: Optional[str] = None,
    label: str = "estimator",
) -> Estimator:
    """estimatr's ``iv_robust``: 2SLS ``y ~ d | z`` with HC2 SEs, df = N − k.

    The sandwich uses the projected regressor matrix X̂ for bread and
    leverage, and the *structural* residuals y − Xβ for the meat — exactly
    estimatr's construction (validated in the fixtures).
    """

    def fn(df: pd.DataFrame) -> EstimatorResult:
        n = len(df)
        yv = df[y].to_numpy(dtype=float)
        Xend = np.column_stack([np.ones(n), df[d].to_numpy(dtype=float)])
        Zins = np.column_stack([np.ones(n), df[z].to_numpy(dtype=float)])
        ZtZinv = np.linalg.inv(Zins.T @ Zins)
        Xhat = Zins @ (ZtZinv @ (Zins.T @ Xend))
        XtXinv = np.linalg.inv(Xhat.T @ Xhat)
        beta = XtXinv @ (Xhat.T @ yv)
        e = yv - Xend @ beta
        h = np.einsum("ij,jk,ik->i", Xhat, XtXinv, Xhat)
        meat = (Xhat * (e**2 / (1.0 - h))[:, None]).T @ Xhat
        V = XtXinv @ meat @ XtXinv
        return _tidy(
            float(beta[1]), float(np.sqrt(V[1, 1])), float(n - 2), term=d, outcome=y
        )

    return Estimator(fn, inquiry=inquiry, label=label)


def _lm_robust_subset_est(
    formula: str,
    subset: Callable[[pd.DataFrame], "pd.Series[bool]"],
    term: Optional[str] = None,
    inquiry: Optional[str] = None,
    label: str = "estimator",
) -> Estimator:
    """``declare_estimator(..., subset = ...)``: lm_robust on a row subset."""

    def fn(df: pd.DataFrame) -> pd.DataFrame:
        sub = df.loc[subset(df)].reset_index(drop=True)
        tidy = _lm_robust_fit(formula, sub)
        return Estimator._pick_terms(tidy, term)

    return Estimator(fn, inquiry=inquiry, label=label)


def _cr2_var_dof(
    X: np.ndarray, resid: np.ndarray, cluster_ids: np.ndarray, j: int
) -> tuple[float, float]:
    """CR2 variance and Bell–McCaffrey df for coefficient ``j`` only.

    Same math as :func:`declarepy.estimators._cluster_robust` (validated in
    T2), restricted to one coefficient so absorbed-fixed-effect designs with
    ~100 dummy columns stay fast enough to diagnose.
    """
    n, k = X.shape
    M = np.linalg.inv(X.T @ X)
    labels, inverse = np.unique(cluster_ids, return_inverse=True)
    G = len(labels)
    idx_by_g = [np.flatnonzero(inverse == g) for g in range(G)]
    A_by_g = []
    meat = np.zeros((k, k))
    for idx in idx_by_g:
        Xg = X[idx]
        Hgg = (Xg @ M) @ Xg.T
        A = _sym_inv_sqrt(np.eye(len(idx)) - Hgg)
        A_by_g.append(A)
        s = Xg.T @ (A @ resid[idx])
        meat += np.outer(s, s)
    V = M @ meat @ M
    H = X @ M @ X.T
    ImH = np.eye(n) - H
    cols = np.empty((n, G))
    for g, (idx, A) in enumerate(zip(idx_by_g, A_by_g)):
        p_g = A @ (X[idx] @ M[:, j])
        cols[:, g] = ImH[:, idx] @ p_g
    omega = cols.T @ cols
    lam = np.linalg.eigvalsh(omega)
    dof = float(lam.sum() ** 2 / (lam**2).sum())
    return float(V[j, j]), dof


def _one_hot_drop_first(values: np.ndarray) -> np.ndarray:
    """0/1 dummy columns for each level after the first (sorted levels)."""
    _, inverse = np.unique(values, return_inverse=True)
    n = len(values)
    n_levels = int(inverse.max()) + 1
    out = np.zeros((n, n_levels - 1))
    mask = inverse > 0
    out[np.arange(n)[mask], inverse[mask] - 1] = 1.0
    return out


def _twfe_est(
    y: str,
    z: str,
    fixed_effects: Sequence[str],
    clusters: str,
    subset: Optional[Callable[[pd.DataFrame], "pd.Series[bool]"]] = None,
    inquiry: Optional[str] = None,
    label: str = "estimator",
) -> Estimator:
    """``lm_robust(y ~ z, fixed_effects = ~ ..., clusters = ...)`` (CR2).

    estimatr's absorbed fixed effects with CR2 cluster SEs are numerically
    identical to including the FE dummies directly (fixture-validated), which
    is what this helper does — with Bell–McCaffrey df computed for the
    treatment coefficient only.
    """

    fes = list(fixed_effects)

    def fn(df: pd.DataFrame) -> EstimatorResult:
        sub = df.loc[subset(df)].reset_index(drop=True) if subset is not None else df
        n = len(sub)
        parts = [np.ones(n), sub[z].to_numpy(dtype=float)]
        for fe in fes:
            parts.append(_one_hot_drop_first(sub[fe].to_numpy()))
        X = np.column_stack(parts)
        yv = sub[y].to_numpy(dtype=float)
        beta, *_ = np.linalg.lstsq(X, yv, rcond=None)
        resid = yv - X @ beta
        vjj, dof = _cr2_var_dof(X, resid, sub[clusters].to_numpy(), j=1)
        return _tidy(float(beta[1]), float(np.sqrt(vjj)), dof, term=z, outcome=y)

    return Estimator(fn, inquiry=inquiry, label=label)



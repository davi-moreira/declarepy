# declaration 18.8 — noncompliance: 2SLS vs as-treated vs per-protocol
# --------------------------------------------------------------------------


def declaration_18_8(N: int = 100) -> Design:
    """declaration_18.8: 20% always-takers, 20% never-takers, 60% compliers.

    Each R estimator answers both inquiries (``inquiry = c("ATE", "CACE")``);
    here each pairing is its own step under the same label. Diagnosed by the
    book as ``diagnosis_18.9``.
    """

    counts = {
        "Always-Taker": int(round(0.2 * N)),
        "Never-Taker": int(round(0.2 * N)),
        "Complier": int(round(0.6 * N)),
        "Defier": 0,
    }
    slopes = {
        "Always-Taker": (-0.25, -0.50),
        "Never-Taker": (0.75, -0.25),
        "Complier": (0.25, 0.50),
        "Defier": (-0.25, -0.50),
    }

    def build(n: int, rng: np.random.Generator) -> dict[str, np.ndarray]:
        types = np.repeat(list(counts.keys()), list(counts.values()))
        return {"type": types, "U": rng.normal(size=n)}

    def po_y(df: pd.DataFrame, z: object, rng: np.random.Generator) -> np.ndarray:
        t = df["type"].to_numpy()
        base = np.select(
            [t == k for k in slopes], [v[0] for v in slopes.values()]
        )
        slope = np.select(
            [t == k for k in slopes], [v[1] for v in slopes.values()]
        )
        result: np.ndarray = base + slope * float(z) + df["U"].to_numpy()  # type: ignore[arg-type]
        return result

    def po_d(df: pd.DataFrame, z: object, rng: np.random.Generator) -> np.ndarray:
        t = df["type"].to_numpy()
        if float(z) == 1:  # type: ignore[arg-type]
            taken = np.isin(t, ["Always-Taker", "Complier"])
        else:
            taken = np.isin(t, ["Always-Taker", "Defier"])
        return taken.astype(int)

    def reveal_d_y(df: pd.DataFrame, rng: np.random.Generator) -> dict[str, object]:
        d = np.where(df["Z"] == 1, df["D_Z_1"], df["D_Z_0"])
        y = np.where(d == 1, df["Y_D_1"], df["Y_D_0"])
        return {"D": d, "Y": y}

    def cace(df: pd.DataFrame) -> float:
        sub = df[df["type"] == "Complier"]
        return float((sub["Y_D_1"] - sub["Y_D_0"]).mean())

    steps: list[object] = [
        Model(n=N, build=build, label="model"),
        potential_outcomes(po_y, outcome="Y", template="{outcome}_D_{condition}"),
        potential_outcomes(po_d, outcome="D", template="{outcome}_Z_{condition}"),
        Inquiry(
            ATE=lambda df: float((df["Y_D_1"] - df["Y_D_0"]).mean()),
            CACE=cace,
        ),
        Assignment.complete(),
        Measurement(reveal_d_y, label="reveal_outcomes(D ~ Z; Y ~ D)"),
    ]
    for inquiry in ("ATE", "CACE"):
        steps.append(
            _iv_robust_est(inquiry=inquiry, label="Two stage least squares")
        )
    for inquiry in ("ATE", "CACE"):
        steps.append(
            Estimator.lm_robust("Y ~ D", inquiry=inquiry, label="As treated")
        )
    for inquiry in ("ATE", "CACE"):
        steps.append(
            _lm_robust_subset_est(
                "Y ~ D", lambda df: df["D"] == df["Z"],
                inquiry=inquiry, label="Per protocol",
            )
        )
    return Design(*steps)  # type: ignore[arg-type]



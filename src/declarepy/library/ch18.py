def _complete_ra_multi(
    N: int, conditions: Sequence[object], rng: np.random.Generator
) -> np.ndarray:
    """Multi-arm complete assignment: ⌊N/K⌋ per arm, remainder to random arms.

    randomizr's ``complete_ra(N, conditions = ...)``: each arm gets the floor
    share; the remaining ``N mod K`` slots go to distinct arms drawn uniformly
    without replacement.
    """
    K = len(conditions)
    base, rem = divmod(N, K)
    counts = np.full(K, base, dtype=int)
    if rem:
        extra = rng.choice(K, size=rem, replace=False)
        counts[extra] += 1
    pool = np.repeat(np.arange(K), counts)
    assignment = np.asarray(conditions, dtype=object)[rng.permutation(pool)]
    return assignment


def _cluster_ra(
    clusters: "pd.Series[Any]",
    conditions: Sequence[object],
    rng: np.random.Generator,
) -> np.ndarray:
    """randomizr's ``cluster_ra``: complete multi-arm assignment of clusters."""
    labels = np.asarray(pd.Series(clusters).to_numpy())
    uniq = pd.unique(labels)
    arm = _complete_ra_multi(len(uniq), conditions, rng)
    lookup = {c: arm[i] for i, c in enumerate(uniq)}
    result: np.ndarray = np.asarray([lookup[c] for c in labels], dtype=object)
    return result


def _block_and_cluster_ra(
    clusters: "pd.Series[Any]",
    blocks: "pd.Series[Any]",
    rng: np.random.Generator,
) -> np.ndarray:
    """randomizr's ``block_and_cluster_ra``: blocked (half) cluster assignment.

    Clusters (in order of first appearance) are grouped by their block label;
    within each block, half of its clusters are treated via ``complete_ra``.
    """
    cl = np.asarray(pd.Series(clusters).to_numpy())
    bl = np.asarray(pd.Series(blocks).to_numpy())
    uniq = pd.unique(cl)
    cluster_block = {c: bl[np.flatnonzero(cl == c)[0]] for c in uniq}
    z_by_cluster: dict[object, int] = {}
    for b in pd.unique(np.asarray([cluster_block[c] for c in uniq], dtype=object)):
        members = [c for c in uniq if cluster_block[c] == b]
        z = _ra.complete_ra(len(members), rng=rng)
        for c, zi in zip(members, z):
            z_by_cluster[c] = int(zi)
    result: np.ndarray = np.asarray([z_by_cluster[c] for c in cl], dtype=int)
    return result


def _strata_rs(
    strata: "pd.Series[Any]",
    strata_n: Mapping[object, int],
    rng: np.random.Generator,
) -> np.ndarray:
    """randomizr's ``strata_rs(strata, strata_n)``: exact-n complete sampling
    within each stratum (``strata_n`` keyed by stratum value)."""
    labels = np.asarray(pd.Series(strata).to_numpy())
    include = np.zeros(len(labels), dtype=int)
    for value, n_s in strata_n.items():
        idx = np.flatnonzero(labels == value)
        include[idx] = _ra.complete_rs(len(idx), n=int(n_s), rng=rng)
    return include


def _block_ra_block_prob(
    blocks: "pd.Series[Any]",
    block_prob: Mapping[object, float],
    rng: np.random.Generator,
) -> np.ndarray:
    """randomizr's ``block_ra(blocks, block_prob)``: per-block treat shares."""
    labels = np.asarray(pd.Series(blocks).to_numpy())
    z = np.zeros(len(labels), dtype=int)
    for value in pd.unique(labels):
        idx = np.flatnonzero(labels == value)
        z[idx] = _ra.complete_ra(len(idx), prob=float(block_prob[value]), rng=rng)
    return z


def _block_ra_prob_unit(
    blocks: "pd.Series[Any]",
    prob_unit: np.ndarray,
    rng: np.random.Generator,
) -> np.ndarray:
    """randomizr's ``block_ra(blocks, prob_unit)``: per-unit probabilities
    (constant within block, as randomizr requires)."""
    labels = np.asarray(pd.Series(blocks).to_numpy())
    probs = np.asarray(prob_unit, dtype=float)
    z = np.zeros(len(labels), dtype=int)
    for value in pd.unique(labels):
        idx = np.flatnonzero(labels == value)
        z[idx] = _ra.complete_ra(len(idx), prob=float(probs[idx[0]]), rng=rng)
    return z



# --------------------------------------------------------------------------
# declaration 18.7 — the 2×2 factorial design (also swept as diagnosis 18.8)
# --------------------------------------------------------------------------


def declaration_18_7(
    N: int = 1000,
    CATE_Z1_Z2_0: float = 0.2,
    CATE_Z2_Z1_0: float = 0.1,
    interaction: float = 0.1,
) -> Design:
    """declaration_18.7: two crossed treatments, six answer strategies.

    Estimators "5" and "6" answer two inquiries from one regression in R
    (``term = c(...), inquiry = c(...)``); here each (term, inquiry) pair is
    its own step under the same label, which diagnoses identically.
    """

    combos = [(z1, z2) for z1 in (0, 1) for z2 in (0, 1)]

    def add_pos(df: pd.DataFrame, rng: np.random.Generator) -> pd.DataFrame:
        out = df.copy()
        u = df["U"].to_numpy()
        for z1, z2 in combos:
            out[f"Y_Z1_{z1}_Z2_{z2}"] = (
                CATE_Z1_Z2_0 * z1 + CATE_Z2_Z1_0 * z2 + interaction * z1 * z2 + u
            )
        return out

    def po_mean(df: pd.DataFrame, a: tuple[int, int], b: tuple[int, int]) -> float:
        ca = f"Y_Z1_{a[0]}_Z2_{a[1]}"
        cb = f"Y_Z1_{b[0]}_Z2_{b[1]}"
        return float((df[ca] - df[cb]).mean())

    def reveal(df: pd.DataFrame, rng: np.random.Generator) -> dict[str, object]:
        conds = [
            (df["Z1"] == z1) & (df["Z2"] == z2) for z1, z2 in combos
        ]
        choices = [df[f"Y_Z1_{z1}_Z2_{z2}"] for z1, z2 in combos]
        return {"Y": np.select(conds, choices)}

    return Design(
        Model(n=N, build=lambda n, rng: {"U": rng.normal(size=n)}, label="model"),
        Model(transform=add_pos, label="potential_outcomes(Y)"),
        Inquiry(
            CATE_Z1_Z2_0=lambda df: po_mean(df, (1, 0), (0, 0)),
            CATE_Z1_Z2_1=lambda df: po_mean(df, (1, 1), (0, 1)),
            ATE_Z1=lambda df: 0.5 * po_mean(df, (1, 0), (0, 0))
            + 0.5 * po_mean(df, (1, 1), (0, 1)),
            CATE_Z2_Z1_0=lambda df: po_mean(df, (0, 1), (0, 0)),
            CATE_Z2_Z1_1=lambda df: po_mean(df, (1, 1), (1, 0)),
            ATE_Z2=lambda df: 0.5 * po_mean(df, (0, 1), (0, 0))
            + 0.5 * po_mean(df, (1, 1), (1, 0)),
            diff_in_CATEs_Z1=lambda df: po_mean(df, (1, 1), (0, 1))
            - po_mean(df, (1, 0), (0, 0)),
            diff_in_CATEs_Z2=lambda df: po_mean(df, (1, 1), (1, 0))
            - po_mean(df, (0, 1), (0, 0)),
        ),
        Assignment.complete(name="Z1"),
        Assignment(
            lambda df, rng: _ra.block_ra(df["Z1"], rng=rng), name="Z2",
            label="block_ra(blocks=Z1)",
        ),
        Measurement(reveal, label="reveal_outcomes(Y ~ Z1 + Z2)"),
        _lm_robust_subset_est(
            "Y ~ Z1", lambda df: df["Z2"] == 0, inquiry="CATE_Z1_Z2_0", label="1"
        ),
        _lm_robust_subset_est(
            "Y ~ Z1", lambda df: df["Z2"] == 1, inquiry="CATE_Z1_Z2_1", label="2"
        ),
        _lm_robust_subset_est(
            "Y ~ Z2", lambda df: df["Z1"] == 0, inquiry="CATE_Z2_Z1_0", label="3"
        ),
        _lm_robust_subset_est(
            "Y ~ Z2", lambda df: df["Z1"] == 1, inquiry="CATE_Z2_Z1_1", label="4"
        ),
        Estimator.lm_robust("Y ~ Z1 + Z2", term="Z1", inquiry="ATE_Z1", label="5"),
        Estimator.lm_robust("Y ~ Z1 + Z2", term="Z2", inquiry="ATE_Z2", label="5"),
        Estimator.lm_robust(
            "Y ~ Z1 + Z2 + Z1:Z2", term="Z1:Z2", inquiry="diff_in_CATEs_Z1", label="6"
        ),
        Estimator.lm_robust(
            "Y ~ Z1 + Z2 + Z1:Z2", term="Z1:Z2", inquiry="diff_in_CATEs_Z2", label="6"
        ),
    )



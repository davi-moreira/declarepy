# declaration 18.1 — the basic two-arm randomized trial (unchanged from T1)
# --------------------------------------------------------------------------


def declaration_18_1(N: int = 100, effect: float = 0.2) -> Design:
    """declaration_18.1: the basic two-arm randomized trial (prob=0.5)."""

    def po(df: pd.DataFrame, z: object, rng: np.random.Generator) -> np.ndarray:
        result: np.ndarray = effect * float(z) + df["U"].to_numpy()  # type: ignore[arg-type]
        return result

    # declare_estimator(Y ~ Z, inquiry = "ATE") with no .method uses
    # DeclareDesign's default, lm_robust (HC2) — numerically the Welch SE
    # for a two-group regression, with df = N − 2.
    return Design(
        Model(n=N, build=lambda n, rng: {"U": rng.normal(size=n)}, label="model"),
        potential_outcomes(po),
        Inquiry("ATE", lambda df: float((df["Y1"] - df["Y0"]).mean())),
        Assignment.complete(prob=0.5),
        reveal_outcomes(),
        Estimator.lm_robust("Y ~ Z", inquiry="ATE"),
    )


# --------------------------------------------------------------------------
# declaration 18.2 — covariate adjustment with the Lin estimator
# --------------------------------------------------------------------------


def declaration_18_2(N: int = 100, r_sq: float = 0.0) -> Design:
    """declaration_18.2: two-arm trial, Lin adjustment for a correlated X.

    ``draw_multivariate(c(U, X) ~ MASS::mvrnorm(...))`` with correlation
    ``sqrt(r_sq)`` becomes a Cholesky transform of two standard normals.
    Swept by ``diagnosis_18.2`` over N and r_sq.
    """

    def build(n: int, rng: np.random.Generator) -> dict[str, np.ndarray]:
        s = float(np.sqrt(r_sq))
        L = np.linalg.cholesky(np.array([[1.0, s], [s, 1.0]]))
        draws = rng.normal(size=(n, 2)) @ L.T
        return {"U": draws[:, 0], "X": draws[:, 1]}

    def po(df: pd.DataFrame, z: object, rng: np.random.Generator) -> np.ndarray:
        result: np.ndarray = 0.1 * float(z) + df["U"].to_numpy()  # type: ignore[arg-type]
        return result

    return Design(
        Model(n=N, build=build, label="model"),
        potential_outcomes(po),
        Inquiry("ATE", lambda df: float((df["Y1"] - df["Y0"]).mean())),
        Assignment.complete(),
        reveal_outcomes(),
        _lm_lin_est(covariates=("X",), inquiry="ATE"),
    )


# --------------------------------------------------------------------------
# declaration 18.3 — DIM vs OLS vs Lin under heterogeneous slopes
# --------------------------------------------------------------------------


def declaration_18_3(prob: float = 0.5, control_slope: float = -1.0) -> Design:
    """declaration_18.3: three adjustment strategies under one treated share.

    Potential outcomes are explicit model columns sharing a single noise
    draw U (unlike ``potential_outcomes()``, which redraws per condition).
    Swept by ``diagnosis_18.3`` over control_slope and prob.
    """

    def build(n: int, rng: np.random.Generator) -> dict[str, np.ndarray]:
        X = rng.uniform(0.0, 1.0, n)
        U = rng.normal(0.0, 0.1, n)
        return {"X": X, "U": U, "Y1": 1.0 * X + U, "Y0": control_slope * X + U}

    return Design(
        Model(n=100, build=build, label="model"),
        Inquiry("ATE", lambda df: float((df["Y1"] - df["Y0"]).mean())),
        Assignment.complete(prob=prob),
        reveal_outcomes(),
        Estimator.lm_robust("Y ~ Z", inquiry="ATE", label="DIM"),
        Estimator.lm_robust("Y ~ Z + X", inquiry="ATE", label="OLS"),
        _lm_lin_est(covariates=("X",), inquiry="ATE", label="Lin"),
    )


# --------------------------------------------------------------------------
# declaration 18.4 — unequal block probabilities with IPW-weighted Lin
# --------------------------------------------------------------------------


def declaration_18_4() -> Design:
    """declaration_18.4: block-varying assignment probabilities + IPW Lin.

    ``block_ra(blocks = X, block_prob = c(0.2, 0.5))`` with design-implied
    condition probabilities (0.2/0.8 in the X = 0 block, 0.5/0.5 in X = 1;
    both block sizes make N·p integral, so the probabilities are exact) and
    ``lm_lin(..., weights = ipw)``. The R declaration declares no inquiry, so
    only estimate-based diagnosands are defined (reference:
    ``rgen_t3_ch18_18.4.json`` — the book's saved diagnosis_18.4.rds is the
    chapter's N = 12 permutation exercise, not a diagnosis of this design).
    """

    def build(n: int, rng: np.random.Generator) -> dict[str, np.ndarray]:
        X = np.repeat([0, 1], n // 2)
        U = rng.normal(0.0, 0.25, n)
        return {"X": X, "U": U}

    def po(df: pd.DataFrame, z: object, rng: np.random.Generator) -> np.ndarray:
        result: np.ndarray = (
            0.2 * float(z) + df["X"].to_numpy() + df["U"].to_numpy()  # type: ignore[arg-type]
        )
        return result

    def probs_ipw(df: pd.DataFrame, rng: np.random.Generator) -> dict[str, object]:
        p1 = np.where(df["X"].to_numpy() == 1, 0.5, 0.2)
        probs = np.where(df["Z"].to_numpy() == 1, p1, 1.0 - p1)
        return {"probs": probs, "ipw": 1.0 / probs}

    return Design(
        Model(n=500, build=build, label="model"),
        potential_outcomes(po),
        Assignment(
            lambda df, rng: _block_ra_block_prob(df["X"], {0: 0.2, 1: 0.5}, rng),
            name="Z",
            label="block_ra(block_prob)",
        ),
        Measurement(probs_ipw, label="condition probabilities"),
        reveal_outcomes(),
        _lm_lin_est(covariates=("X",), weights="ipw", label="Lin"),
    )


# --------------------------------------------------------------------------
# declaration 18.5 — blocked-and-clustered assignment, CR2 inference
# --------------------------------------------------------------------------


def declaration_18_5(ICC: float = 0.9) -> Design:
    """declaration_18.5: 10 clusters of unequal size, blocked on size.

    The fabricatr two-level hierarchy is built explicitly: cluster-level
    draws are expanded to individuals with ``np.repeat``. Swept by
    ``diagnosis_18.5`` over ICC.
    """

    sizes = np.tile(np.arange(10, 51, 10), 2)  # rep(seq(10, 50, 10), 2)
    n_total = int(sizes.sum())

    def build(n: int, rng: np.random.Generator) -> pd.DataFrame:
        n_cl = len(sizes)
        cluster_shock = _scale(sizes + rng.normal(0.0, 5.0, n_cl)) * np.sqrt(ICC)
        cluster_tau = rng.normal(0.0, np.sqrt(ICC), n_cl)
        cl = np.repeat(np.arange(1, n_cl + 1), sizes)
        ind_shock = rng.normal(0.0, np.sqrt(1.0 - ICC), n)
        ind_tau = rng.normal(0.0, np.sqrt(1.0 - ICC), n)
        y0 = cluster_shock[cl - 1] + ind_shock
        y1 = y0 + cluster_tau[cl - 1] + ind_tau
        return pd.DataFrame(
            {
                "cluster": cl,
                "cluster_size": np.repeat(sizes, sizes),
                "Y0": y0,
                "Y1": y1,
            }
        )

    return Design(
        Model(n=n_total, build=build, label="model"),
        Inquiry("ATE", lambda df: float((df["Y1"] - df["Y0"]).mean())),
        Assignment(
            lambda df, rng: _block_and_cluster_ra(df["cluster"], df["cluster_size"], rng),
            name="Z",
            label="block_and_cluster_ra",
        ),
        reveal_outcomes(),
        Estimator.lm_robust("Y ~ Z", clusters="cluster", inquiry="ATE"),
    )



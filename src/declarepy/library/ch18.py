def declaration_18_12() -> Design:
    """declaration_18.12: group-level saturation, unit-level treatment.

    Groups are cluster-assigned to low/high saturation; within groups,
    units are treated with probability 0.25 (low) or 0.75 (high). The two
    estimators are a clustered and a blocked difference in means.
    """

    n_groups, group_size = 50, 20

    def build(n: int, rng: np.random.Generator) -> pd.DataFrame:
        group_shock = rng.normal(size=n_groups)
        group = np.repeat(np.arange(1, n_groups + 1), group_size)
        ind_shock = rng.normal(size=n)
        return pd.DataFrame(
            {
                "group": group,
                "group_shock": group_shock[group - 1],
                "individual_shock": ind_shock,
            }
        )

    def add_pos(df: pd.DataFrame, rng: np.random.Generator) -> pd.DataFrame:
        out = df.copy()
        shocks = df["group_shock"].to_numpy() + df["individual_shock"].to_numpy()
        for z in (0, 1):
            for s in ("low", "high"):
                out[f"Y_Z_{z}_S_{s}"] = (
                    0.2 * z + 0.1 * (s == "low") + 0.5 * (s == "high") + shocks
                )
        return out

    def reveal(df: pd.DataFrame, rng: np.random.Generator) -> dict[str, object]:
        conds = []
        choices = []
        for z in (0, 1):
            for s in ("low", "high"):
                conds.append((df["Z"] == z) & (df["S"] == s))
                choices.append(df[f"Y_Z_{z}_S_{s}"])
        return {"Y": np.select(conds, choices)}

    def dim_high_vs_low_untreated(df: pd.DataFrame) -> EstimatorResult:
        sub = df.loc[df["Z"] == 0, ["Y", "S", "group"]].copy()
        sub["Shigh"] = (sub["S"] == "high").astype(int)
        res = _dim_fit(sub, y="Y", z="Shigh", clusters="group")
        res.term = "Shigh"
        return res

    def dim_treated_low(df: pd.DataFrame) -> EstimatorResult:
        sub = df.loc[df["S"] == "low", ["Y", "Z", "group"]].copy()
        return _dim_fit(sub, y="Y", z="Z", blocks="group")

    return Design(
        Model(n=n_groups * group_size, build=build, label="model"),
        Model(transform=add_pos, label="potential_outcomes(Y)"),
        Inquiry(
            CATE_S_Z_0=lambda df: float(
                (df["Y_Z_0_S_high"] - df["Y_Z_0_S_low"]).mean()
            ),
            CATE_Z_S_low=lambda df: float(
                (df["Y_Z_1_S_low"] - df["Y_Z_0_S_low"]).mean()
            ),
        ),
        Assignment(
            lambda df, rng: _cluster_ra(df["group"], ["low", "high"], rng),
            name="S",
            label="cluster_ra(conditions=low/high)",
        ),
        Assignment(
            lambda df, rng: _block_ra_prob_unit(
                df["group"],
                np.where(df["S"].to_numpy() == "low", 0.25, 0.75),
                rng,
            ),
            name="Z",
            label="block_ra(prob_unit)",
        ),
        Measurement(reveal, label="reveal_outcomes(Y ~ Z + S)"),
        Estimator(
            dim_high_vs_low_untreated,
            inquiry="CATE_S_Z_0",
            label="Effect of high saturation among untreated",
        ),
        Estimator(
            dim_treated_low,
            inquiry="CATE_Z_S_low",
            label="Effect of treatment at low saturation",
        ),
    )


# --------------------------------------------------------------------------
# declaration 18.13 — experiments over networks (Aronow–Samii)
# --------------------------------------------------------------------------

_AS_CONDITIONS = ("dir_ind1", "isol_dir", "ind1", "no")


def _exposure_as(z: np.ndarray, adj: np.ndarray) -> np.ndarray:
    """interference's hop-1 A–S exposure condition of each unit."""
    treated = np.asarray(z) > 0
    peer = (np.asarray(z, dtype=float) @ adj) > 0
    result: np.ndarray = np.select(
        [treated & peer, treated & ~peer, ~treated & peer],
        ["dir_ind1", "isol_dir", "ind1"],
        default="no",
    )
    return result


def _as_exposure_probs(
    adj: np.ndarray, permutations: int, prob: float, rng: np.random.Generator
) -> dict[str, np.ndarray]:
    """Per-unit generalized exposure probabilities from a permutation matrix.

    interference's ``make_exposure_prob``: π_i(k) = (count over R permuted
    assignments of unit i in condition k, + 1) / (R + 1).
    """
    n = adj.shape[0]
    counts = {k: np.zeros(n) for k in _AS_CONDITIONS}
    for _ in range(permutations):
        z = _ra.complete_ra(n, prob=prob, rng=rng)
        expo = _exposure_as(z, adj)
        for k in _AS_CONDITIONS:
            counts[k] += expo == k
    return {k: (counts[k] + 1.0) / (permutations + 1.0) for k in _AS_CONDITIONS}


def _as_estimator_step(
    kind: str, term: str, pi: dict[str, np.ndarray], inquiry: str, label: str
) -> Estimator:
    """One Horvitz–Thompson or Hajek contrast (condition ``term`` vs 'no').

    interference's ``estimates()``: yT_ht(k) = Σ_{i∈k} y_i / π_i(k);
    tau_ht(k) = (yT_ht(k) − yT_ht(no)) / N; the Hajek version normalizes by
    Σ_{i∈k} 1/π_i(k). An empty condition yields NaN (R: NA).
    """

    def totals(df: pd.DataFrame, k: str) -> tuple[float, float]:
        mask = (df["exposure"] == k).to_numpy()
        if not mask.any():
            return float("nan"), float("nan")
        yv = df.loc[mask, "Y"].to_numpy(dtype=float)
        p = pi[k][mask]
        return float((yv / p).sum()), float((1.0 / p).sum())

    def fn(df: pd.DataFrame) -> dict[str, object]:
        y_k, w_k = totals(df, term)
        y_no, w_no = totals(df, "no")
        if kind == "ht":
            est = (y_k - y_no) / len(df)
        else:
            est = y_k / w_k - y_no / w_no
        return {"estimate": est, "term": term, "outcome": "Y"}

    return Estimator(fn, inquiry=inquiry, label=label)


def declaration_18_13(
    permutations: int = 10000, permutation_seed: int = 464
) -> Design:
    """declaration_18.13: spillovers on the Fairfax precinct map.

    Potential outcomes indexed by (own treatment, any-treated-neighbor):
    Y_{z, peer}. Exposure probabilities are estimated once per factory call
    from ``permutations`` fresh complete assignments (the R source's
    ``obtain_permutation_matrix(..., 10000)``, drawn with a dedicated seed).
    The book's reference records point-estimate diagnosands only (the
    published estimator returns no standard errors), so power/coverage are
    undefined, matching R.
    """

    fair = _load_csv("fairfax")
    adj = _load_csv("fairfax_adjacency", header=False).to_numpy(dtype=float)
    data = pd.DataFrame({"SHAPE_LEN": fair["SHAPE_LEN"].to_numpy(dtype=float)})
    y00 = stats.norm.cdf(_scale(data["SHAPE_LEN"].to_numpy()), scale=3.0)
    data["Y_0_0"] = y00
    data["Y_1_0"] = y00 + 0.02
    data["Y_0_1"] = y00 + 0.01
    data["Y_1_1"] = y00 + 0.03

    pi = _as_exposure_probs(
        adj, permutations, prob=0.1, rng=np.random.default_rng(permutation_seed)
    )

    def reveal(df: pd.DataFrame, rng: np.random.Generator) -> dict[str, object]:
        expo = df["exposure"]
        y = np.select(
            [expo == "dir_ind1", expo == "isol_dir", expo == "ind1", expo == "no"],
            [df["Y_1_1"], df["Y_1_0"], df["Y_0_1"], df["Y_0_0"]],
        )
        return {"Y": y}

    steps: list[object] = [
        Model(data=data, label="model"),
        Inquiry(
            total_ATE=lambda df: float((df["Y_1_1"] - df["Y_0_0"]).mean()),
            direct_ATE=lambda df: float((df["Y_1_0"] - df["Y_0_0"]).mean()),
            indirect_ATE=lambda df: float((df["Y_0_1"] - df["Y_0_0"]).mean()),
        ),
        Assignment(
            lambda df, rng: _ra.complete_ra(len(df), prob=0.1, rng=rng),
            name="Z",
            label="complete_ra(prob=0.1)",
        ),
        Assignment(
            lambda df, rng: _exposure_as(df["Z"].to_numpy(), adj),
            name="exposure",
            label="exposure_AS(hop=1)",
        ),
        Measurement(reveal, label="reveal Y by exposure"),
    ]
    for kind, est_label in (("ht", "Horvitz-Thompson"), ("hajek", "Hajek")):
        for term, inquiry in (
            ("dir_ind1", "total_ATE"),
            ("isol_dir", "direct_ATE"),
            ("ind1", "indirect_ATE"),
        ):
            steps.append(_as_estimator_step(kind, term, pi, inquiry, est_label))
    return Design(*steps)  # type: ignore[arg-type]

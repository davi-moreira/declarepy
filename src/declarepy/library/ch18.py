def _scale(x: np.ndarray) -> np.ndarray:
    """R's ``scale()``: center and divide by the sample sd (ddof = 1)."""
    arr = np.asarray(x, dtype=float)
    result: np.ndarray = (arr - arr.mean()) / arr.std(ddof=1)
    return result


def _tidy(
    est: float, se: float, dof: float, term: str, outcome: str, alpha: float = 0.05
) -> EstimatorResult:
    """t-based inference row from an estimate, its SE and its df."""
    tstat = est / se if se > 0 else float("nan")
    p = float(2 * stats.t.sf(abs(tstat), dof)) if se > 0 else float("nan")
    crit = float(stats.t.ppf(1 - alpha / 2, dof))
    return EstimatorResult(
        estimate=est, std_error=se, statistic=tstat, p_value=p,
        conf_low=est - crit * se, conf_high=est + crit * se, df=dof,
        term=term, outcome=outcome,
    )


def _hc2_core(
    X: np.ndarray, y: np.ndarray, w: Optional[np.ndarray] = None
) -> tuple[np.ndarray, np.ndarray, float]:
    """OLS/WLS with HC2 SEs, estimatr-style: (beta, se, df = n − k).

    With weights, estimatr applies the HC2 leverage correction to the
    √w-transformed design matrix and residuals (validated digit-for-digit
    against ``lm_robust``/``lm_lin`` with ``weights=`` in the fixtures).
    """
    n, k = X.shape
    if w is None:
        Xs, ys = X, y
    else:
        sw = np.sqrt(w)
        Xs, ys = X * sw[:, None], y * sw
    XtXinv = np.linalg.inv(Xs.T @ Xs)
    beta: np.ndarray = XtXinv @ (Xs.T @ ys)
    resid = ys - Xs @ beta
    h = np.einsum("ij,jk,ik->i", Xs, XtXinv, Xs)
    meat = (Xs * (resid**2 / (1.0 - h))[:, None]).T @ Xs
    V = XtXinv @ meat @ XtXinv
    return beta, np.sqrt(np.diag(V)), float(n - k)



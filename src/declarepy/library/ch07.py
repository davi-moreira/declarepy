"""Chapter 7 declarations: three flavors of a mean inquiry, no estimator.

Provenance: replication-materials ``code/declarations/declaration_7.1.R``
(Blair, Coppock & Humphreys 2023). The book ships no saved diagnosis object
for this declaration; the validation reference is a fresh 2000-sim R run of
the unmodified book declaration with estimand-focused diagnosands
(``validation/reference/rgen_t3_ch04_05_07_7.1.json``, generated by
``validation/r_scripts/t3_ch04_05_07_reference.R``).
"""

from __future__ import annotations

from typing import Optional

import numpy as np
import pandas as pd

from ..diagnose import run_design
from ..steps import Design, Inquiry, Model, Sampling

__all__ = ["declaration_7_1"]


def declaration_7_1(N: int = 20, n: int = 10) -> Design:
    """declaration_7.1: superpopulation, population, and sample mean of Y.

    R source: ``declare_model(N = 20, U = rnorm(N), Y = 1 + U)`` +
    ``declare_inquiry(superpopulation_mean = 1, population_mean = mean(Y))``
    + ``complete_rs(N, n = 10)`` + ``declare_inquiry(sample_mean =
    mean(Y))``. There is deliberately no estimator: the design contrasts
    three targets of inference — the fixed superpopulation mean (exactly 1),
    the realized population mean over the N draws, and the mean among the n
    sampled units (position matters: the last inquiry sits after sampling).
    """

    def build(n_: int, rng: np.random.Generator) -> dict[str, np.ndarray]:
        U = rng.normal(size=n_)
        return {"U": U, "Y": 1.0 + U}

    return Design(
        Model(n=N, build=build, label="model"),
        Inquiry(
            superpopulation_mean=lambda df: 1.0,
            population_mean=lambda df: float(df["Y"].mean()),
        ),
        Sampling.complete(n=n),
        Inquiry("sample_mean", lambda df: float(df["Y"].mean())),
    )


def _diagnose_estimands(
    design: Design, sims: int = 2000, seed: Optional[int] = 464
) -> pd.DataFrame:
    """Estimand-only diagnosis: mean and sd of every inquiry across sims.

    declarepy's :func:`declarepy.diagnose` summarizes estimate rows, so a
    design with inquiries but no estimator (like declaration_7.1) yields an
    empty table — whereas DeclareDesign's ``diagnose_design`` handles
    estimand-only designs natively. This helper fills the gap: it runs the
    design ``sims`` times through ONE ``default_rng(seed)`` stream (the same
    reproducibility contract as :func:`declarepy.diagnose`) and returns one
    row per inquiry with ``mean_estimand`` and ``sd_estimand``. Candidate
    for promotion into ``declarepy.diagnose``.
    """
    gen = np.random.default_rng(seed)
    rows: list[dict[str, object]] = []
    for i in range(sims):
        run = run_design(design, rng=gen)
        for inq, val in run.estimands.items():
            rows.append({"sim": i, "inquiry": inq, "estimand": val})
    sims_df = pd.DataFrame(rows)
    grouped = sims_df.groupby("inquiry", sort=False)["estimand"]
    means = grouped.mean()
    sds = grouped.std(ddof=1)
    out = pd.DataFrame(
        {
            "inquiry": means.index.to_list(),
            "mean_estimand": means.to_numpy(),
            "sd_estimand": sds.to_numpy(),
        }
    )
    out["n_sims"] = sims
    return out

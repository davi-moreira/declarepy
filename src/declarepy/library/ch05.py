"""Chapter 5 declarations: a descriptive inquiry on a latent subgroup.

Provenance: replication-materials ``code/declarations/declaration_5.1.R``
(Blair, Coppock & Humphreys 2023). The book ships no saved diagnosis object
for this declaration; the validation reference is a fresh 2000-sim R run of
the unmodified book declaration
(``validation/reference/rgen_t3_ch04_05_07_5.1.json``, generated by
``validation/r_scripts/t3_ch04_05_07_reference.R``).
"""

from __future__ import annotations

from typing import Callable

import numpy as np
import pandas as pd
from scipy import stats

from ..estimators import lm_robust as _lm_robust
from ..steps import Design, Estimator, Inquiry, Model, Sampling

__all__ = ["declaration_5_1"]


def _lm_robust_subset(
    formula: str,
    subset: Callable[[pd.DataFrame], "pd.Series[bool]"],
) -> Callable[[pd.DataFrame], pd.DataFrame]:
    """Estimator fn mirroring ``declare_estimator(..., subset = <cond>)``.

    DeclareDesign estimators take a ``subset`` argument that filters the
    frame before fitting; declarepy's :class:`Estimator` factories have no
    such argument yet, so this private helper wraps :func:`lm_robust` with
    the filter applied first. Candidate for promotion into
    ``Estimator.lm_robust(subset=...)``.
    """

    def run(df: pd.DataFrame) -> pd.DataFrame:
        sub = df.loc[np.asarray(subset(df), dtype=bool)].reset_index(drop=True)
        # A sampled subgroup can be degenerate (e.g. every Y = 1), making the
        # intercept-only fit's residual variance zero: se = 0, t = inf, p = 0
        # and a point CI — exactly estimatr's behavior, but numpy warns on
        # the 0-divides along the way. Silence just those.
        with np.errstate(divide="ignore", invalid="ignore"):
            return _lm_robust(formula, sub)

    return run


def declaration_5_1(N: int = 1000, prob: float = 0.1) -> Design:
    """declaration_5.1: mean of Y among X = 1, from a simple random sample.

    R source: ``declare_model(N = 1000, U = rnorm(N), X = rbinom(N, 1,
    prob = pnorm(U)), Y = rbinom(N, 1, prob = pnorm(U + X)))`` +
    ``declare_inquiry(Ybar = mean(Y[X == 1]))`` (a population estimand,
    declared before sampling) + ``simple_rs(N, prob = 0.1)`` +
    ``lm_robust(Y ~ 1, subset = X == 1)``. The intercept-only regression on
    the sampled X = 1 units estimates the population subgroup mean; ``pnorm``
    is the standard-normal CDF (``scipy.stats.norm.cdf``).
    """

    def build(n_: int, rng: np.random.Generator) -> dict[str, np.ndarray]:
        U = rng.normal(size=n_)
        X = rng.binomial(1, stats.norm.cdf(U), n_)
        Y = rng.binomial(1, stats.norm.cdf(U + X), n_)
        return {"U": U, "X": X, "Y": Y}

    return Design(
        Model(n=N, build=build, label="model"),
        Inquiry("Ybar", lambda df: float(df.loc[df["X"] == 1, "Y"].mean())),
        Sampling.simple(prob=prob),
        Estimator(
            _lm_robust_subset("Y ~ 1", lambda df: df["X"] == 1),
            inquiry="Ybar",
            label="estimator",
        ),
    )

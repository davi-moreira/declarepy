def _lh_robust_test(
    formula: str, data: pd.DataFrame, linear_hypothesis: str, alpha: float = 0.05
) -> pd.DataFrame:
    """The linear-hypothesis row of estimatr's ``lh_robust``.

    ``lh_robust(formula, data, linear_hypothesis)`` fits ``lm_robust`` (HC2)
    and runs ``car::linearHypothesis``; for the single-restriction form
    ``"<term> = <value>"`` implemented here (all this chapter needs), the lh
    row is ``estimate = beta_term − value`` with the term's HC2 standard
    error, the FIRST coefficient's df (estimatr uses ``lm_robust_fit$df[1]``),
    and t-based inference. Validated against ``lh_robust`` /
    its ``car::linearHypothesis`` internals in
    ``rgen_t3_ch09_lh_robust.json`` (the installed estimatr 1.0.6 cannot run
    ``lh_robust`` on single-coefficient fits — a guard-condition bug — so the
    intercept-only reference reproduces its internals verbatim).
    """
    lhs, sep, rhs = linear_hypothesis.partition("=")
    if not sep:
        raise ValueError(
            f"only '<term> = <value>' hypotheses are supported, got {linear_hypothesis!r}"
        )
    term = lhs.strip()
    null_value = float(rhs)
    lookup = "Intercept" if term == "(Intercept)" else term
    tidy = _est.lm_robust(formula, data, alpha=alpha)
    match = tidy[tidy["term"] == lookup]
    if not len(match):
        raise KeyError(f"term {term!r} not in fit: {list(tidy['term'])}")
    est = float(match["estimate"].iloc[0]) - null_value
    se = float(match["std_error"].iloc[0])
    dof = float(tidy["df"].iloc[0])
    stat = est / se
    p = float(2 * stats.t.sf(abs(stat), dof))
    crit = float(stats.t.ppf(1 - alpha / 2, dof))
    return pd.DataFrame(
        [
            {
                "term": linear_hypothesis,
                "estimate": est,
                "std_error": se,
                "statistic": stat,
                "p_value": p,
                "conf_low": est - crit * se,
                "conf_high": est + crit * se,
                "df": dof,
                "outcome": formula.split("~")[0].strip(),
            }
        ]
    )



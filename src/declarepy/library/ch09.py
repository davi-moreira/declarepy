def _block_and_cluster_ra(
    blocks: pd.Series,
    clusters: pd.Series,
    block_m: Mapping[object, int],
    rng: np.random.Generator,
) -> np.ndarray:
    """randomizr ``block_and_cluster_ra(blocks, clusters, block_m)``.

    Clusters are nested in blocks; within each block, exactly
    ``block_m[block]`` whole clusters are treated (complete random
    assignment at the cluster level), then cluster assignments broadcast to
    units. Iterates blocks in sorted order for a deterministic stream.
    """
    frame = pd.DataFrame({"b": np.asarray(blocks), "c": np.asarray(clusters)})
    cluster_blocks = frame.drop_duplicates("c")
    z_by_cluster: dict[object, int] = {}
    for b in np.sort(cluster_blocks["b"].unique()):
        cl_in_b = cluster_blocks.loc[cluster_blocks["b"] == b, "c"].to_numpy()
        z_b = _ra.complete_ra(len(cl_in_b), m=int(block_m[b]), rng=rng)
        for c, z in zip(cl_in_b, z_b):
            z_by_cluster[c] = int(z)
    return frame["c"].map(z_by_cluster).to_numpy(dtype=int)


def declaration_9_7() -> Design:
    """declaration_9.7: sharp-null randomization distribution, Foos et al.

    R: fixed ``foos_etal`` data; ``potential_outcomes(Y ~ 0 * Z +
    marked_register_2014)`` (the sharp null — both potential outcomes equal
    the observed 2014 turnout); ``block_and_cluster_ra(blocks = ward,
    clusters = street, block_m)`` with per-ward treated-street counts;
    inverse-probability weights from ``obtain_condition_probabilities``
    (cluster-level ``block_m / n_streets`` per ward); ``lm_robust(Y ~ Z +
    ward, weights = ipw, clusters = street)`` (CR2). Translation note: R
    computes Z, probs and ipw inside one ``declare_assignment``; here the
    same D step is an Assignment (Z) plus a Measurement (probs, ipw). The
    design declares no inquiry — the book diagnoses it with a custom
    ``p.value`` diagnosand against the observed estimate.
    """
    data = _load_data("foos_etal")
    wards = np.sort(data["ward"].unique())
    block_m: dict[object, int] = dict(zip(wards, _BLOCK_M_9_7))
    n_streets = data.groupby("ward")["street"].nunique()
    p_ward: dict[object, float] = {
        w: block_m[w] / float(n_streets[w]) for w in wards
    }

    def add_probs(df: pd.DataFrame, rng: np.random.Generator) -> dict[str, object]:
        p_treat = df["ward"].map(p_ward).to_numpy(dtype=float)
        probs = np.where(df["Z"].to_numpy() == 1, p_treat, 1.0 - p_treat)
        return {"probs": probs, "ipw": 1.0 / probs}

    return Design(
        Model(data=data),
        potential_outcomes(
            lambda df, z, rng: df["marked_register_2014"].to_numpy(dtype=float)
        ),
        Assignment(
            lambda df, rng: _block_and_cluster_ra(
                df["ward"], df["street"], block_m, rng
            ),
            name="Z",
            label="block_and_cluster_ra(ward, street, block_m)",
        ),
        Measurement(add_probs, label="condition_probabilities"),
        reveal_outcomes(),
        Estimator(
            lambda df: _lm_robust_weighted(
                "Y ~ Z + ward", df, weights="ipw", clusters="street"
            ),
            label="estimator",
        ),
    )

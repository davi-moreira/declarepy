def _stan_glm_log_gaussian(
    y: np.ndarray,
    prior_loc: float = 50.0,
    prior_scale: float = 5.0,
    n_alpha: int = 801,
    n_sigma: int = 401,
) -> tuple[float, float]:
    """Posterior (median, MAD-SD) of exp(intercept) for a log-link gaussian GLM.

    Reproduces rstanarm's ``stan_glm(y ~ 1, family = gaussian(link = "log"),
    prior_intercept = normal(prior_loc, prior_scale))`` followed by
    ``tidy_stan(., exponentiate = TRUE)`` (broom.mixed: point estimate =
    posterior median, uncertainty = MAD-SD):

    * likelihood: ``y_i ~ Normal(exp(alpha), sigma)``;
    * ``alpha ~ Normal(prior_loc, prior_scale)`` on the linear-predictor
      (log) scale — rstanarm applies user-specified ``prior_intercept``
      as-is (autoscale defaults to FALSE);
    * ``sigma ~ Exponential(1/sd(y))`` — rstanarm's default ``prior_aux``
      exponential(1), autoscaled by sd(y) (guarded to 1 when sd(y) = 0).

    Instead of MCMC, the 2-parameter posterior is integrated on a
    deterministic (alpha, log sigma) grid, so a given sample always yields
    the identical estimate (no chain noise). Returns
    ``(exp(median(alpha)), MAD-SD of exp(alpha)))`` — exponentiating the
    posterior median equals the median of the exponentiated draws.
    """
    y = np.asarray(y, dtype=float)
    n = len(y)
    sd_y = float(np.std(y, ddof=1)) if n > 1 else 0.0
    scale_sigma = sd_y if sd_y > 0 else 1.0
    rate = 1.0 / scale_sigma

    # Grid ranges generous enough for 3 draws of ages in [0, 80] under the
    # N(50, 5) log-scale prior pull (posterior mu well inside (0.05, 500)).
    alpha = np.linspace(np.log(0.05), np.log(500.0), n_alpha)
    log_sigma = np.linspace(np.log(0.05), np.log(3000.0), n_sigma)
    sigma = np.exp(log_sigma)

    mu = np.exp(alpha)
    ssr = ((y[None, :] - mu[:, None]) ** 2).sum(axis=1)  # (n_alpha,)
    # log joint on the grid (integrating over sigma with d sigma = sigma d log
    # sigma, hence the +log_sigma Jacobian term).
    ll = (
        -n * log_sigma[None, :]
        - ssr[:, None] / (2.0 * sigma[None, :] ** 2)
        - rate * sigma[None, :]
        + log_sigma[None, :]
    )
    lp_alpha = -((alpha - prior_loc) ** 2) / (2.0 * prior_scale**2)
    lj = ll + lp_alpha[:, None]
    m = float(lj.max())
    marg = np.exp(lj - m).sum(axis=1)  # unnormalized p(alpha | y) on the grid
    cdf = np.cumsum(marg)
    cdf /= cdf[-1]
    # Deterministic pseudo-draws by inverse-CDF at evenly spaced quantiles.
    qs = (np.arange(1, 4000) - 0.5) / 3999.0
    draws_alpha = np.interp(qs, cdf, alpha)
    med = float(np.median(draws_alpha))
    est = float(np.exp(med))
    exp_draws = np.exp(draws_alpha)
    mad_sd = float(1.4826 * np.median(np.abs(exp_draws - est)))
    return est, mad_sd


def declaration_9_3() -> Design:
    """declaration_9.3: Bayesian mean-age estimate with a Normal(50, 5) prior.

    R: ``declare_estimator(age ~ 1, .method = stan_glm, family =
    gaussian(link = "log"), prior_intercept = normal(50, 5), .summary =
    ~tidy_stan(., exponentiate = TRUE), inquiry = "mean_age")`` on the
    declaration-9.1 model/sampling. The estimator reports only a point
    estimate and a MAD-SD (no p-value or CI), so the book's diagnosis has
    NA power and coverage.
    """

    def stan_est(df: pd.DataFrame) -> pd.DataFrame:
        est, se = _stan_glm_log_gaussian(df["age"].to_numpy(dtype=float))
        return pd.DataFrame(
            [{"term": "(Intercept)", "estimate": est, "std_error": se, "outcome": "age"}]
        )

    return Design(
        Model(
            n=100,
            build=lambda n, rng: {"age": rng.integers(0, 81, n)},
            label="model",
        ),
        Inquiry("mean_age", lambda df: float(df["age"].mean())),
        Sampling.complete(n=3),
        Estimator(stan_est, inquiry="mean_age", label="estimator"),
    )



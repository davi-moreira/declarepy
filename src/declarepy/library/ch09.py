def _block_ra_probs(
    blocks: Union[pd.Series, np.ndarray],
    block_prob: Sequence[float],
    rng: np.random.Generator,
) -> np.ndarray:
    """randomizr ``block_ra(blocks, block_prob=)``: per-block probabilities.

    ``block_prob`` maps to ``sort(unique(blocks))`` (randomizr's rule,
    verified against randomizr 1.0); within each block the number treated is
    ``floor(n_b * p_b)`` or its ceiling with probability equal to the
    fractional part — :func:`declarepy.ra.complete_ra`'s ``prob`` rule.
    """
    labels = np.asarray(pd.Series(blocks).to_numpy())
    uniq = np.sort(pd.unique(labels))
    if len(uniq) != len(block_prob):
        raise ValueError(
            f"block_prob has {len(block_prob)} entries for {len(uniq)} blocks"
        )
    z = np.zeros(len(labels), dtype=int)
    for lab, p in zip(uniq, block_prob):
        idx = np.flatnonzero(labels == lab)
        z[idx] = _ra.complete_ra(len(idx), prob=float(p), rng=rng)
    return z


def _dd_default_term(names: Sequence[str], formula: str) -> str:
    """DeclareDesign's default term: the first non-intercept coefficient.

    R's model matrix keeps the formula's own term order, but patsy places
    categorical expansions before numeric terms — so resolve the default
    from the formula text (first right-hand-side term), falling back to the
    first non-Intercept column.
    """
    first = formula.split("~", 1)[1].split("+")[0].strip()
    if first in names:
        return first
    for nm in names:
        if nm != "Intercept":
            return nm
    return names[0]


def _pick_weighted_terms(
    tidy: pd.DataFrame, term: Optional[Union[str, Sequence[str]]]
) -> pd.DataFrame:
    """Select the reported term rows from the weighted-lm tidy frame."""
    if term == "all":
        return tidy
    if term is None:
        non_int = tidy[tidy["term"] != "Intercept"]
        return (non_int.iloc[:1] if len(non_int) else tidy.iloc[:1]).copy()
    wanted = [term] if isinstance(term, str) else list(term)
    out = tidy[tidy["term"].isin(wanted)].copy()
    if len(out) != len(wanted):
        missing = set(wanted) - set(out["term"])
        raise KeyError(f"terms not in fit: {sorted(missing)}")
    return out


def _lm_robust_weighted(
    formula: str,
    data: pd.DataFrame,
    weights: str,
    clusters: Optional[str] = None,
    term: Optional[Union[str, Sequence[str]]] = None,
    alpha: float = 0.05,
) -> pd.DataFrame:
    """estimatr-style weighted ``lm_robust``: HC2, or CR2 + BM df with clusters.

    WLS point estimates with estimatr's weighted robust inference,
    transcribed from estimatr's ``lm_robust_helper.cpp`` / ``prep_data``:

    * rows are scaled by ``sqrt(w / mean(w))``; HC2 (no clusters) applies the
      leverage correction on the weighted design matrix with ``N − k`` df;
    * with ``clusters=``, the CR2 bias-reduced linearization uses the
      cluster blocks of ``B_g = I − H_g − H_g' + X_g (M U'U M) X_g'``
      (working covariance = identity on the unweighted scale, exactly
      estimatr's construction) and Bell–McCaffrey/Satterthwaite df, computed
      here only for the reported term(s).

    Validated coefficient-for-coefficient (estimates, SEs, df, p, CIs)
    against estimatr in ``rgen_t3_ch09_wls_hc2.json``,
    ``rgen_t3_ch09_wls_cr2.json`` and ``rgen_t3_ch09_foos_observed.json``.
    ``term=None`` reports the first non-intercept term (DeclareDesign's
    default); pass a name, list of names, or ``"all"``.
    """
    import statsmodels.formula.api as smf

    model = smf.ols(formula, data=data)
    if model.exog.shape[0] < len(data):
        n_dropped = len(data) - model.exog.shape[0]
        raise ValueError(
            f"{n_dropped} rows with missing values in the model frame; drop or "
            "impute explicitly before estimating (declarepy never skips NAs silently)"
        )
    X = np.asarray(model.exog, dtype=float)
    y = np.asarray(model.endog, dtype=float)
    names = list(model.exog_names)
    w = data[weights].to_numpy(dtype=float)
    if np.isnan(w).any():
        raise ValueError(f"missing values in weights column {weights!r}")
    n, k = X.shape
    if term is None:
        term = _dd_default_term(names, formula)

    # estimatr's prep_data: scale rows by sqrt(w / mean(w)).
    wm = float(w.mean())
    wn = w / wm
    sw = np.sqrt(wn)
    Xt = X * sw[:, None]
    yt = y * sw
    M = np.linalg.inv(Xt.T @ Xt)
    beta = M @ (Xt.T @ yt)

    outcome = formula.split("~")[0].strip()
    base = pd.DataFrame({"term": names, "estimate": beta})
    if clusters is None:
        resid_t = yt - Xt @ beta
        h = np.einsum("ij,ij->i", Xt @ M, Xt)
        omega = resid_t**2 / (1.0 - h)
        omega[~np.isfinite(omega)] = 0.0  # perfect fits, as in estimatr
        meat = Xt.T @ (omega[:, None] * Xt)
        V = M @ meat @ M
        base["std_error"] = np.sqrt(np.diag(V))
        base["df"] = float(n - k)
        picked = _pick_weighted_terms(base, term)
    else:
        if data[clusters].isna().any():
            raise ValueError(f"missing values in cluster column {clusters!r}")
        e = y - X @ beta  # unweighted residuals (estimatr's CR2-weighted input)
        Xw2 = X * wn[:, None]
        MUWTWUM = M @ (Xw2.T @ Xw2) @ M
        L = np.linalg.cholesky(M)
        Oc = np.linalg.cholesky(MUWTWUM)
        picked_no_se = _pick_weighted_terms(base, term)
        terms_j = [names.index(t) for t in picked_no_se["term"]]
        _, inverse = np.unique(data[clusters].to_numpy(), return_inverse=True)
        G = int(inverse.max()) + 1
        idx_by_g = [np.flatnonzero(inverse == g) for g in range(G)]
        meat = np.zeros((k, k))
        H1t = {j: np.empty((k, G)) for j in terms_j}
        H2t = {j: np.empty((k, G)) for j in terms_j}
        H3t = {j: np.empty((k, G)) for j in terms_j}
        Pdg = {j: np.empty(G) for j in terms_j}
        for g, idx in enumerate(idx_by_g):
            Xg = X[idx]
            Xw2g = Xw2[idx]
            Hg = Xg @ M @ Xw2g.T
            B = np.eye(len(idx)) - Hg - Hg.T + Xg @ MUWTWUM @ Xg.T
            AX = _est._sym_inv_sqrt(B) @ Xw2g  # estimatr's At_WX_inv
            s = AX.T @ e[idx]
            meat += np.outer(s, s)
            ME = (M / wm) @ AX.T
            MEU = ME @ Xg
            MEX = ME @ Xw2g
            for j in terms_j:
                Pdg[j][g] = float((ME[j] ** 2).sum())
                H1t[j][:, g] = L.T @ MEU[j]
                H2t[j][:, g] = L.T @ MEX[j]
                H3t[j][:, g] = Oc.T @ MEU[j]
        V = M @ meat @ M
        base["std_error"] = np.sqrt(np.diag(V))
        base["df"] = np.nan
        for j in terms_j:
            uf = H1t[j].T @ H2t[j]
            P_array = H3t[j].T @ H3t[j] - uf - uf.T + np.diag(Pdg[j])
            base.loc[j, "df"] = float(
                np.trace(P_array) ** 2 / (P_array**2).sum()
            )
        picked = _pick_weighted_terms(base, term)

    est = picked["estimate"].to_numpy(dtype=float)
    se = picked["std_error"].to_numpy(dtype=float)
    dof = picked["df"].to_numpy(dtype=float)
    tstat = est / se
    pval = 2 * stats.t.sf(np.abs(tstat), dof)
    crit = stats.t.ppf(1 - alpha / 2, dof)
    return pd.DataFrame(
        {
            "term": picked["term"].to_numpy(),
            "estimate": est,
            "std_error": se,
            "statistic": tstat,
            "p_value": pval,
            "conf_low": est - crit * se,
            "conf_high": est + crit * se,
            "df": dof,
            "outcome": outcome,
        }
    )


_BLOCK_PROB_9_6 = (0.1, 0.8)


def _model_and_inquiry_9_6() -> Design:
    """declaration_9.6's shared MI: X ~ Bern(0.5), heterogeneous effect, ATE."""

    def po(df: pd.DataFrame, z: object, rng: np.random.Generator) -> np.ndarray:
        x = df["X"].to_numpy(dtype=float)
        result: np.ndarray = (
            0.5 * float(z) - 0.5 * x + 0.5 * x * float(z) + df["U"].to_numpy()  # type: ignore[arg-type]
        )
        return result

    return Design(
        Model(
            n=100,
            build=lambda n, rng: {
                "X": rng.binomial(1, 0.5, n),
                "U": rng.normal(size=n),
            },
            label="model",
        ),
        potential_outcomes(po),
        Inquiry("ATE", lambda df: float((df["Y1"] - df["Y0"]).mean())),
    )


def _ipw_measurement_9_6() -> Measurement:
    """A2's ipw step: 1 / obtain_condition_probabilities(Z, blocks=X, block_prob).

    The weights ASSUME blocked assignment with ``block_prob = c(0.1, 0.8)``
    whether or not the design actually assigned that way — the point of the
    book's third/fourth comparison.
    """

    def add_ipw(df: pd.DataFrame, rng: np.random.Generator) -> dict[str, object]:
        bp = np.asarray(_BLOCK_PROB_9_6, dtype=float)
        p_treat = bp[df["X"].to_numpy(dtype=int)]
        prob = np.where(df["Z"].to_numpy() == 1, p_treat, 1.0 - p_treat)
        return {"ipw": 1.0 / prob}

    return Measurement(add_ipw, label="ipw")


def declaration_9_6() -> list[Design]:
    """declaration_9.6: the four MI + (D1|D2) + (A1|A2) combinations.

    R declares a shared model/inquiry (MI), two data strategies — D1
    ``complete_ra`` and D2 ``block_ra(blocks = X, block_prob = c(0.1,
    0.8))`` — and two answer strategies — A1 unweighted ``lm_robust(Y ~ Z)``
    and A2 inverse-probability-weighted ``lm_robust(Y ~ Z, weights = ipw)``
    with weights computed under the D2 assumption. Returns the book's list
    order: ``[MI+D1+A1, MI+D1+A2, MI+D2+A1, MI+D2+A2]`` (diagnose with
    ``dp.diagnose_all``, which labels them design_1 … design_4). The R
    estimators declare no inquiry; DeclareDesign's diagnosis joins the lone
    ATE estimand to every row, so the translation attaches
    ``inquiry="ATE"`` explicitly.
    """
    d1 = Design(Assignment.complete(), reveal_outcomes())
    d2 = Design(
        Assignment(
            lambda df, rng: _block_ra_probs(df["X"], _BLOCK_PROB_9_6, rng),
            name="Z",
            label="block_ra(blocks=X, block_prob=(0.1, 0.8))",
        ),
        reveal_outcomes(),
    )
    a1 = Estimator.lm_robust("Y ~ Z", inquiry="ATE", label="Unweighted")

    def a2() -> Design:
        return Design(
            _ipw_measurement_9_6(),
            Estimator(
                lambda df: _lm_robust_weighted("Y ~ Z", df, weights="ipw"),
                inquiry="ATE",
                label="Weighted",
            ),
        )

    return [
        _model_and_inquiry_9_6() + d1 + a1,
        _model_and_inquiry_9_6() + d1 + a2(),
        _model_and_inquiry_9_6() + d2 + a1,
        _model_and_inquiry_9_6() + d2 + a2(),
    ]


